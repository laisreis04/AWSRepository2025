#Solicitar Largura (User)
largura = float(input("Insira a largura : "))
#Solicitar Altura (User)
altura = float(input("Insira a altura : "))


#Calcular área perimetro
area = largura * altura
perimetro = 2 * (largura+ altura)



#Impimir resultados
print("Área:" , area)
print("Perímetro:" , perimetro)