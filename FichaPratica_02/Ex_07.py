#Input 
numeroInicio = int(input("Insira um número: "))

numAnterior = numeroInicio - 5
while numAnterior < numeroInicio:
    print(numAnterior)
    numAnterior += 1

# Imprimir acima do númeor inicial
numSeguinte = numeroInicio + 1
while numSeguinte <= numeroInicio + 5 :
    print(numSeguinte)
    numSeguinte += 1
